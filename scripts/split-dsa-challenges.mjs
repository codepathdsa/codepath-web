/**
 * split-dsa-challenges.mjs
 *
 * Splits lib/challenges/dsa.ts (one giant array) into:
 *   lib/challenges/dsa/<ID>.ts  — one file per challenge
 *   lib/challenges/dsa/index.ts — barrel that re-exports all as dsaChallenges[]
 *
 * Run once: node scripts/split-dsa-challenges.mjs
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const SRC  = join(ROOT, 'lib', 'challenges', 'dsa.ts');
const DEST = join(ROOT, 'lib', 'challenges', 'dsa');

const src = readFileSync(SRC, 'utf-8');

// ─── Locate the opening [ of the array ────────────────────────────────────────
const arrayStart = src.indexOf('export const dsaChallenges: Challenge[] = [');
if (arrayStart === -1) throw new Error('Could not find dsaChallenges array');
const contentStart = src.indexOf('[', arrayStart) + 1;

// ─── Brace-depth walk to extract each top-level object ───────────────────────
const challenges = [];
let depth  = 0;
let inStr  = false;
let strCh  = '';
let escape = false;
let objStart = -1;

for (let i = contentStart; i < src.length; i++) {
  const ch = src[i];

  // Track string literals so we don't count braces inside them
  if (!inStr) {
    if (ch === '"' || ch === "'" || ch === '`') {
      inStr = true;
      strCh = ch;
      continue;
    }
    if (ch === '{') {
      if (depth === 0) objStart = i;          // start of a top-level object
      depth++;
    } else if (ch === '}') {
      depth--;
      if (depth === 0 && objStart !== -1) {
        challenges.push(src.slice(objStart, i + 1));
        objStart = -1;
      } else if (depth < 0) {
        break;                                 // hit the closing ] of the array
      }
    }
  } else {
    // Handle escape sequences inside strings
    if (escape) { escape = false; continue; }
    if (ch === '\\') { escape = true; continue; }
    if (ch === strCh) inStr = false;
  }
}

console.log(`Found ${challenges.length} challenges.`);

// ─── Ensure output directory exists ──────────────────────────────────────────
if (!existsSync(DEST)) mkdirSync(DEST, { recursive: true });

// ─── Write one file per challenge ────────────────────────────────────────────
const ids = [];
const skipped = [];

for (const block of challenges) {
  // Extract the id field: id: 'ENG-DSA-001'
  const m = block.match(/\bid\s*:\s*['"]([^'"]+)['"]/);
  if (!m) { skipped.push(block.slice(0, 60)); continue; }
  const id = m[1];
  ids.push(id);

  const fileName = `${id}.ts`;
  const filePath = join(DEST, fileName);

  // Indent the object body by removing common leading whitespace so it
  // exports cleanly with 2-space indent
  const lines = block.split('\n');
  // Find minimum indentation (ignore empty lines)
  const minIndent = lines
    .filter(l => l.trim().length > 0)
    .reduce((min, l) => Math.min(min, l.match(/^(\s*)/)[1].length), Infinity);
  const dedented = lines.map(l => l.slice(minIndent)).join('\n');

  const fileContent =
`import type { Challenge } from '../types';

// ─── ${id} ─────────────────────────────────────────────────────────────────
// To edit this challenge, just modify the object below.
// Run \`npx next build\` after saving to confirm no TypeScript errors.

const challenge: Challenge = ${dedented};

export default challenge;
`;

  writeFileSync(filePath, fileContent, 'utf-8');
  console.log(`  ✓ ${fileName}`);
}

if (skipped.length) {
  console.warn(`  ⚠ Skipped ${skipped.length} blocks (no id found):`, skipped);
}

// ─── Write the barrel index ───────────────────────────────────────────────────
const importLines = ids
  .map((id, i) => `import c${String(i + 1).padStart(3, '0')} from './${id}';`)
  .join('\n');

const exportLine =
  `export const dsaChallenges = [\n` +
  ids.map((_, i) => `  c${String(i + 1).padStart(3, '0')},`).join('\n') +
  `\n];\n`;

const barrelContent =
`// AUTO-GENERATED by scripts/split-dsa-challenges.mjs
// Do not edit this file by hand.
// To add a new challenge: create lib/challenges/dsa/ENG-DSA-NNN.ts and add
// it to the imports + array below.

import type { Challenge } from '../types'; // eslint-disable-line @typescript-eslint/no-unused-vars
${importLines}

${exportLine}`;

writeFileSync(join(DEST, 'index.ts'), barrelContent, 'utf-8');
console.log(`  ✓ dsa/index.ts  (${ids.length} challenges)`);

// ─── Summary ─────────────────────────────────────────────────────────────────
console.log(`\nDone! Next steps:`);
console.log(`  1. Update lib/challenges/dsa.ts to re-export from ./dsa:`);
console.log(`     export { dsaChallenges } from './dsa';`);
console.log(`  2. Run: npx next build`);
